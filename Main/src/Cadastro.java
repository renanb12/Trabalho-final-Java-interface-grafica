    private void btnGerarRelatorioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGerarRelatorioActionPerformed
        // TODO add your handling code here:
        try {
            String caminho = System.getProperty("user.home") + "/Desktop/relatório.txt";
            File arquivo = new File(caminho);

            FileWriter fw = new FileWriter(arquivo);
            BufferedWriter escrever = new BufferedWriter(fw);

            DefaultTableModel modelo = (DefaultTableModel) tbHorarios.getModel();

            StringBuilder relatorio = new StringBuilder();
            relatorio.append("Relatório de Clientes:\n");
            relatorio.append("------------------------------\n");
            relatorio.append("NOME TUTOR   |      TELEFONE    |  NOME PET  |  ESPÉCIE  |  RAÇA   |     COR       |   SERVIÇO  | ATENDIMENTO\n");

            for (int i = 0; i < modelo.getRowCount(); i++) {
                for (int j = 0; j < modelo.getColumnCount(); j++) {
                    relatorio.append(modelo.getValueAt(i, j)).append("\t");
                }
                relatorio.append("\n");
            }

            escrever.write(relatorio.toString());
            escrever.close();
            JOptionPane.showMessageDialog(this, "Relatório criado.");
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Ocorreu um erro ao escrever no arquivo: " + e.getMessage());
        }
    }//GEN-LAST:event_btnGerarRelatorioActionPerformed

